#include<unordered_map>
#include<algorithm>
#include<vector>
#include<iostream>
using namespace std;
class Solution {
public:
    int longestOnes(vector<int>& nums, int k) {
        int left=0,res=0;
        int count=0;
        for(int right=0;right<nums.size();right++)
        {
            if(nums[right]==0)
                count++;
            while(count>k)
            {
                if(nums[left]==0)
                    count--;
                left++;
            }
            res = max(res,right-left+1);
        }
        return res;
    }
};